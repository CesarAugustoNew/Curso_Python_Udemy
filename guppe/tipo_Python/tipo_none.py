"""
Tipo None

O tipo de dado None em Python representa o tipo sem tipo, ou poderia ser conhecido também como tipo vazio, porém, falar que é um topo sem tipo é mais aporpriado

OBS: O tipo None é sempre especificado com a primeira letra maiuscula

Certo: None
Errado: none

- Podemos utilizar None quando criar uma variavel e inicializa-la com um tipo sem tipo, antes de recebermos um valor final,

- OBS: O tipo None em Python é sempre considerado como False

"""

numeros = None
print(numeros)
print(type(numeros))

numeros = 1.44, 1.34, 5,42

print(numeros)
print(type(numeros))